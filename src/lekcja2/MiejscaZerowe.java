package lekcja2;

public class MiejscaZerowe {
    private double piersze;
    private double drugie;

    public MiejscaZerowe(double piersze, double drugie) {
        this.piersze = piersze;
        this.drugie = drugie;
    }

    public double getPiersze() {
        return piersze;
    }

    public double getDrugie() {
        return drugie;
    }
}
